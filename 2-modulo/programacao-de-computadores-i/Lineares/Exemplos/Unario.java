public class Unario {
public static void main (String [] args){
	int F;
	
	System.out.println();
	
	//Incremento: ++
	F = 0;
	
	//Pós-incremento
	System.out.println(F++ +2);
	
	//Pré-Incremento
	System.out.println(++F +2);
	
	//Decremento: --
	F = 10;
	
	//Pós-decremento
	System.out.println (F-- +2);
	
	//Pré-decremento
	System.out.println (--F +2);
 }
}