public class Atribuicao{
	public static void main (String [] args){
		int x=10;
		int y=5;
	System.out.println();
	System.out.println("-----------");
	System.out.println(" X vale " + x + "\n Y vale " + y);
	x+=y;
	System.out.println("-----------");
	System.out.println();
	System.out.println("=============");
	System.out.println("X+=y vale " + x);
	x-=y;
	System.out.println("X-=y vale " + x);
	x*=y;
	System.out.println("X*=y vale " + x);
	x/=y;
	System.out.println("X/=y vale " + x);
	System.out.println("=============");
	}
}